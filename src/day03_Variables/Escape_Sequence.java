package day03_Variables;

import java.sql.SQLOutput;

public class Escape_Sequence {

    public static void main(String[] args) {
        System.out.println("Hello everyone");
        System.out.println("My name is Cybertek, I am from Virgina");


        System.out.println("=============================");

        System.out.println("Hello everyone\nMy name is Cybertek, I am from Virgina");


        // \t:
        System.out.println("\tToday I learned Java");
        System.out.println("It was so much fun");

        System.out.println("==============================");

        // \\:  \
        System.out.println("/"); // single front back splash
        System.out.println("\\"); // print single backslash

        System.out.println("=================================");

        System.out.println("My name is 'Muhammad'");
        System.out.println("My name is \'Muhammad\'");

        System.out.println("===============================");
// \" Double quote
        System.out.println("My favortie TV show is \"Games of Throne\" ");


    }

}




